'use client';

// IMPORTS
// -----------------------------------------------------------------------------------------
import { createContext, useContext } from 'react';
// -----------------------------------------------------------------------------------------


// CONTEXT
// -----------------------------------------------------------------------------------------
export const Context_app = createContext<{

  is_mobile: boolean;
  is_offline: boolean;
  screen_width: number;
  size_screen: 'sm' | 'md' | 'lg';

}
>({

  is_mobile: false,
  is_offline: false,
  screen_width: 0,
  size_screen: 'lg',

});
// -----------------------------------------------------------------------------------------



// SHORTHAND
// -----------------------------------------------------------------------------------------
export const useApp = () => useContext(Context_app);
// -----------------------------------------------------------------------------------------